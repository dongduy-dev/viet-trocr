def print_summary() -> None:
    print("\n" + "═" * 55)
    print("TỔNG KẾT")
    print("═" * 55)
    print(f"  OK (đã lưu)  : {STATS['ok']}")
    print(f"  Skip label   : {STATS['skip_label']}")
    print(f"  Skip image   : {STATS['skip_image']}")
    print(f"  Ảnh hỏng     : {STATS['corrupted']}")

    print("\n  Output:")
    for level in ["word", "line", "paragraph"]:
        csv_p = OUT / level / "labels_master.csv"
        if csv_p.exists():
            with open(csv_p, "r", encoding="utf-8") as f:
                rows = list(csv.DictReader(f))
            hw = sum(1 for r in rows if r.get("data_type") == "handwritten")
            pt = sum(1 for r in rows if r.get("data_type") == "printed")
            print(f"    processed/{level}/labels_master.csv : "
                  f"{len(rows)} dòng  (HW={hw}, PT={pt})")

    print("\n  Bước tiếp theo: python scripts/02_split.py")


