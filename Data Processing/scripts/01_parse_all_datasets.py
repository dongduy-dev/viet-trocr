"""
01_parse_all_datasets.py
========================
Xử lý toàn bộ dữ liệu Handwritten + Printed.

Cấu trúc raw_data mong đợi:
  raw_data/
  ├── HandWritten/
  │   ├── Cinnamon_AI_Dataset/
  │   ├── UIT_HWDB/
  │   └── viet_wiki/
  └── Printed/
      ├── VinText_Cropped/
      ├── Vietnamese Receipts MC_OCR 2021/
      ├── anyuuus - Vietnamese OCR with PaddleOCR/
      └── Synthetic_Modern/             (generated by Generate_Synthetic_Printed_Data)

Schema CSV (labels_master.csv):
  filename | label | source | level | data_type | writer_id | pre_split

pre_split logic:
  Cinnamon  : Data1->'val' | Data2->'train' | Private_Test->'test'
  UIT_HWDB  : train_data->'train_pool'      | test_data->'test'
  Viet_Wiki : 'unassigned'  (sklearn sẽ chia trong 02_split.py)
  VinText   : train_images->'train' | test_image->'val' | unseen_test_images->'test'
  MC_OCR    : train txt->'train'    | val txt->'val'
  Anyuuus   : 'anyuuus_pool'        (group-split theo writer_id trong 02_split.py)
  Synthetic : 'synthetic_pool'       (random split 90/5/5 trong 02_split.py)

Cài đặt : pip install Pillow tqdm
Chạy    : python scripts/01_parse_all_datasets.py [uit|cinnamon|wiki|vintext|mcocr|anyuuus|synthetic|printed|all]

WARNING: Khi chạy lại script, tất cả labels_master.csv sẽ bị xóa và tạo lại
         từ đầu để tránh lặp dữ liệu (append-mode duplicate).
"""

import csv
import json
import sys
import unicodedata
import re
from pathlib import Path

from PIL import Image, UnidentifiedImageError
from tqdm import tqdm

# ── Đường dẫn gốc ────────────────────────────────────────────────────────────
PROJECT_ROOT = Path(__file__).resolve().parent.parent
RAW          = PROJECT_ROOT / "raw_data"
RAW_HW       = RAW / "HandWritten"   # dữ liệu chữ viết tay
RAW_PT       = RAW / "Printed"       # dữ liệu chữ in
OUT          = PROJECT_ROOT / "processed"
VOCAB_DIR    = PROJECT_ROOT / "vocab"

# ── Tập ký tự toàn cục ───────────────────────────────────────────────────────
GLOBAL_VOCAB: set[str] = set()

# ── Schema CSV ───────────────────────────────────────────────────────────────
FIELDNAMES = ["filename", "label", "source", "level", "data_type", "writer_id", "pre_split"]

# ── Thống kê ─────────────────────────────────────────────────────────────────
STATS = {"ok": 0, "skip_label": 0, "skip_image": 0, "corrupted": 0}


# ─────────────────────────────────────────────────────────────────────────────
