def normalize_label(text: str) -> str | None:
    """
    Chuẩn hóa label văn bản:
      1. Strip khoảng trắng đầu/cuối
      2. Thu gọn khoảng trắng bên trong thành 1 space
      3. Unicode NFC  (bắt buộc cho tiếng Việt)
    Trả về None nếu label rỗng sau chuẩn hóa.
    """
    if not text:
        return None
    text = re.sub(r"\s+", " ", text.strip())
    text = unicodedata.normalize("NFC", text)
    return text if text else None


def save_image(src: Path, dst: Path) -> bool:
    """
    Mở ảnh -> chuyển RGB -> lưu PNG.
    Tự động ép extension đầu ra thành .png (vì format luôn là PNG).
    Trả về True nếu OK, False nếu file hỏng / không đọc được.
    """
    dst = dst.with_suffix(".png")  # ép extension khớp format thực tế
    try:
        with Image.open(src) as img:
            img.convert("RGB").save(dst, format="PNG")
        return True
    except (UnidentifiedImageError, OSError, Exception):
        return False


def append_to_master_csv(rows: list[dict], level: str) -> None:
    """Append rows vào processed/{level}/labels_master.csv."""
    if not rows:
        return
    out_path = OUT / level / "labels_master.csv"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    write_header = not out_path.exists()
    with open(out_path, "a", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        if write_header:
            writer.writeheader()
        writer.writerows(rows)


def delete_all_master_csvs() -> None:
    """
    Xóa tất cả labels_master.csv trước khi parse để tránh lặp dữ liệu.
    Vì append_to_master_csv() dùng mode 'a', chạy lại sẽ duplicate toàn bộ.
    """
    for level in ["word", "line", "paragraph"]:
        csv_path = OUT / level / "labels_master.csv"
        if csv_path.exists():
            csv_path.unlink()
            print(f"  [DELETED] {csv_path}")


def update_vocab(label: str) -> None:
    """Thu thập tất cả ký tự trong label vào GLOBAL_VOCAB."""
    GLOBAL_VOCAB.update(label)


def save_vocab() -> None:
    """
    Lưu tập ký tự duy nhất ra vocab/vietnamese_vocab.txt.
    Sắp xếp theo thứ tự Unicode để dễ kiểm tra và debug.

    WARNING: GLOBAL_VOCAB chỉ chứa ký tự từ các parser được chạy trong
    lần gọi hiện tại. Nếu chạy từng parser riêng lẻ (e.g. 'uit' rồi
    'vintext'), file vocab sẽ bị ghi đè và chỉ chứa ký tự từ lần chạy
    cuối cùng. Để có vocab đầy đủ, luôn chạy với target='all'.
    """
    VOCAB_DIR.mkdir(parents=True, exist_ok=True)
    vocab_path = VOCAB_DIR / "vietnamese_vocab.txt"
    sorted_chars = sorted(GLOBAL_VOCAB, key=ord)
    with open(vocab_path, "w", encoding="utf-8") as f:
        for ch in sorted_chars:
            f.write(ch + "\n")
    print(f"\n[VOCAB] {len(sorted_chars)} ký tự duy nhất -> {vocab_path}")


def make_row(filename: str, label: str, source: str, level: str,
             data_type: str, writer_id: str, pre_split: str) -> dict:
    """Tạo dict row theo đúng FIELDNAMES schema."""
    return {
        "filename":  filename,
        "label":     label,
        "source":    source,
        "level":     level,
        "data_type": data_type,
        "writer_id": writer_id,
        "pre_split": pre_split,
    }


