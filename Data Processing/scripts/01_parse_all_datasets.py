# ── 5. MC_OCR 2021 ───────────────────────────────────────────────────────────

def _extract_mcocr_writer_id(img_name: str) -> str:
    """
    Trích xuất Document ID (Invoice ID) từ tên file MC_OCR.
    Ví dụ: 'mcocr_public_145013aagqw_0.jpg' -> 'mcocr_public_145013aagqw'
    Logic: bỏ phần '_<số>.jpg' ở cuối.
    """
    stem = Path(img_name).stem          # bỏ extension
    parts = stem.rsplit("_", maxsplit=1)
    # Nếu phần cuối là số nguyên thì bỏ đi
    if len(parts) == 2 and parts[1].isdigit():
        return parts[0]
    return stem


def parse_mcocr() -> None:
    """
    LINE level - chữ in từ hóa đơn/biên lai thực tế.
    Cấu trúc:
      Vietnamese Receipts MC_OCR 2021/
        text_recognition_mcocr_data/    <- thư mục chứa ảnh
        text_recognition_train_data.txt <- "img.jpg\\tlabel"
        text_recognition_val_data.txt   <- "img.jpg\\tlabel"
    writer_id = Document ID trích từ tên file (chống data leakage theo tài liệu).
    """
    print("\n" + "═" * 55)
    print("[PT 2/3] MC_OCR 2021  (line - printed)")
    print("═" * 55)

    (OUT / "line" / "images").mkdir(parents=True, exist_ok=True)

    base     = RAW_PT / "Vietnamese Receipts MC_OCR 2021"
    img_dir  = base / "text_recognition_mcocr_data"

    if not base.exists():
        print(f"  [SKIP] {base}")
        return
    if not img_dir.exists():
        print(f"  [SKIP] {img_dir}")
        return

    # (label_txt_file, pre_split)
    txt_configs = [
        ("text_recognition_train_data.txt", "train"),
        ("text_recognition_val_data.txt",   "val"),
    ]

    rows: list[dict] = []
    for txt_name, pre_split in txt_configs:
        txt_path = base / txt_name
        if not txt_path.exists():
            print(f"  [SKIP] {txt_path}")
            continue

        # Đọc file label
        label_map: dict[str, str] = {}
        with open(txt_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.rstrip("\n")
                if "\t" not in line:
                    continue
                img_name, raw_label = line.split("\t", maxsplit=1)
                label_map[img_name.strip()] = raw_label.strip()

        count = 0
        for img_name, raw_label in tqdm(label_map.items(),
                                         desc=f"  {txt_name[:35]}", unit="img"):
            label = normalize_label(raw_label)
            if label is None:
                STATS["skip_label"] += 1
                continue

            src = img_dir / img_name
            if not src.exists():
                STATS["skip_image"] += 1
                continue

            writer_id = _extract_mcocr_writer_id(img_name)
            new_name  = Path(f"mcocr_{img_name}").with_suffix(".png").name
            dst       = OUT / "line" / "images" / new_name

            if not save_image(src, dst):
                STATS["corrupted"] += 1
                continue

            update_vocab(label)
            rows.append(make_row(new_name, label, "mcocr", "line",
                                  "printed", writer_id, pre_split))
            STATS["ok"] += 1
            count += 1

        print(f"    {txt_name}: {count} mẫu  (pre_split='{pre_split}')")

    append_to_master_csv(rows, "line")
    print(f"  → {len(rows)} mẫu  →  processed/line/labels_master.csv")


# ── 6. ANYUUUS - PaddleOCR ───────────────────────────────────────────────────

def _extract_anyuuus_writer_id(img_relative_path: str) -> str:
    """
    Trích xuất Document ID từ đường dẫn ảnh Anyuuus.
    Ví dụ: 'final_crop/213_crop_0.jpg' -> '213'
    Logic: lấy stem của filename, split theo '_crop', lấy phần đầu.
    """
    stem = Path(img_relative_path).stem    # '213_crop_0'
    return stem.split("_crop")[0]          # '213'


def parse_anyuuus() -> None:
    """
    LINE level - chữ in từ văn bản scan.
    Cấu trúc: anyuuus - Vietnamese OCR with PaddleOCR/{doc_id}/
      rec_gt.txt   : "crop_img/213_crop_0.jpg\\tlabel"
      final_crop/  : thư mục chứa ảnh thực tế
    Sửa path: thay 'crop_img/' -> 'final_crop/' khi tìm ảnh.
    pre_split = 'anyuuus_pool' -> 02_split.py group-split theo writer_id.
    writer_id = Document ID (số tài liệu, e.g. '213').
    """
    print("\n" + "═" * 55)
    print("[PT 3/3] ANYUUUS - PaddleOCR  (line - printed)")
    print("═" * 55)

    (OUT / "line" / "images").mkdir(parents=True, exist_ok=True)

    base = RAW_PT / "anyuuus - Vietnamese OCR with PaddleOCR"
    if not base.exists():
        print(f"  [SKIP] {base}")
        return

    # Liệt kê các thư mục con (mỗi folder là 1 tập tài liệu)
    doc_dirs = sorted([d for d in base.iterdir() if d.is_dir()])
    if not doc_dirs:
        print(f"  [SKIP] Không tìm thấy thư mục con trong {base}")
        return

    rows: list[dict] = []
    for doc_dir in tqdm(doc_dirs, desc="  Anyuuus doc folders", unit="folder"):
        rec_gt = doc_dir / "rec_gt.txt"
        if not rec_gt.exists():
            continue

        with open(rec_gt, "r", encoding="utf-8") as f:
            for line in f:
                line = line.rstrip("\n")
                if "\t" not in line:
                    continue

                img_path_raw, raw_label = line.split("\t", maxsplit=1)
                img_path_raw = img_path_raw.strip()

                # Sửa path: 'crop_img/213_crop_0.jpg' -> 'final_crop/213_crop_0.jpg'
                img_path_fixed = img_path_raw.replace("crop_img/", "final_crop/")
                src = doc_dir / img_path_fixed

                label = normalize_label(raw_label)
                if label is None:
                    STATS["skip_label"] += 1
                    continue

                if not src.exists():
                    STATS["skip_image"] += 1
                    continue

                writer_id = _extract_anyuuus_writer_id(img_path_fixed)
                img_stem  = Path(img_path_fixed).name
                new_name  = f"anyuuus_{doc_dir.name}_{img_stem}"
                # Đảm bảo extension là .png
                new_name  = Path(new_name).with_suffix(".png").name
                dst       = OUT / "line" / "images" / new_name

                if not save_image(src, dst):
                    STATS["corrupted"] += 1
                    continue

                update_vocab(label)
                rows.append(make_row(new_name, label, "anyuuus", "line",
                                      "printed", writer_id, "anyuuus_pool"))
                STATS["ok"] += 1

    append_to_master_csv(rows, "line")
    print(f"  → {len(rows)} mẫu  →  processed/line/labels_master.csv")


# ── 7. SYNTHETIC MODERN ──────────────────────────────────────────────────────

def parse_synthetic() -> None:
    """
    LINE level - dữ liệu in tổng hợp từ Generate_Synthetic_Printed_Data pipeline.
    Cấu trúc: Printed/Synthetic_Modern/
      images/          <- thư mục chứa ảnh .png
      labels.txt       <- "synth_000000.png\tlabel"
    pre_split = 'synthetic_pool' -> 02_split.py chia ngẫu nhiên 90/5/5.
    writer_id = 'synthetic' (dữ liệu tổng hợp, không có writer).
    """
    print("\n" + "═" * 55)
    print("[PT 4/4] SYNTHETIC MODERN  (line - printed)")
    print("═" * 55)

    (OUT / "line" / "images").mkdir(parents=True, exist_ok=True)

    base = RAW_PT / "Synthetic_Modern"
    if not base.exists():
        print(f"  [SKIP] {base}")
        return

    img_dir   = base / "images"
    label_file = base / "labels.txt"

    if not img_dir.exists() or not label_file.exists():
        print(f"  [SKIP] images/ hoặc labels.txt không tồn tại trong {base}")
        return

    # Đọc labels.txt: "synth_000000.png\tlabel"
    label_map: dict[str, str] = {}
    with open(label_file, "r", encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n")
            if "\t" not in line:
                continue
            img_name, raw_label = line.split("\t", maxsplit=1)
            label_map[img_name.strip()] = raw_label.strip()

    rows: list[dict] = []
    for img_name, raw_label in tqdm(label_map.items(),
                                     desc="  Synthetic_Modern", unit="img"):
        label = normalize_label(raw_label)
        if label is None:
            STATS["skip_label"] += 1
            continue

        src = img_dir / img_name
        if not src.exists():
            STATS["skip_image"] += 1
            continue

        # Giữ nguyên tên file — prefix synth_ đã có sẵn
        base_name = f"synth_{img_name}" if not img_name.startswith("synth_") else img_name
        new_name  = Path(base_name).with_suffix(".png").name
        dst       = OUT / "line" / "images" / new_name

        if not save_image(src, dst):
            STATS["corrupted"] += 1
            continue

        update_vocab(label)
        rows.append(make_row(new_name, label, "synth_modern", "line",
                              "printed", "synthetic", "synthetic_pool"))
        STATS["ok"] += 1

    append_to_master_csv(rows, "line")
    print(f"  → {len(rows)} mẫu  →  processed/line/labels_master.csv")


